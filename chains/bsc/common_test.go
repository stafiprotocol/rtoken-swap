package bsc_test

import (
	"encoding/hex"
	"math/big"
	"rtoken-swap/chains/bsc"
	"testing"

	"github.com/ethereum/go-ethereum/common"
)

func TestGetProposalHash(t *testing.T) {
	hash := bsc.GetProposalHash(big.NewInt(1516), []common.Address{common.HexToAddress("0xce23F6b2D1f2D9D8E32518817050501669e9709F")}, []*big.Int{big.NewInt(100)})
	t.Log(hex.EncodeToString(hash[:]))
}

